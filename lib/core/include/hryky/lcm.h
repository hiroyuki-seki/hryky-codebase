namespace hryky
{
	/// computes the L.C.M..
	template <typename ValueT>
	ValueT lcm(ValueT const lhs, ValueT const rhs);

} // namespace hryky
//------------------------------------------------------------------------------
// defines global functions
//------------------------------------------------------------------------------
/**
  @brief computes the L.C.M..
 */
template <typename ValueT>
ValueT hryky::lcm(ValueT const lhs, ValueT const rhs)
{
	return lhs * rhs / gcd(lhs, rhs);
}
